                <h2 class="text-center text-lg text-uppercase my-0">Moisturize Your Skin
                </h2>
                <hr class="divider">
                <p>
                    Moisturize your skin twice a day, in the morning when you wake up and evening before you go to bed. This may differ based
                    on your daily practices but I choose to do it twice a day. During the day we are all busy with our lives,
                    we are in the sun or cold and we need to keep those skin pores closed. I believe that moisturizing your
                    skin twice a day stimulates your skin cells while hydrating your skin and unclogging your pores. You
                    will have to perform this process moderately as overdoing it may cause skin irritation and dry skin
                </p>
