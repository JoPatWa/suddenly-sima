                <h2 class="text-center text-lg text-uppercase my-0">No Need For Winter To Keep You Down

                </h2>

                <hr class="divider">

                <p>I remember growing up reading the American books and the characters there would note how fun it is to be

                    outside sledding on the snow or snow tubing down the hill and making snow angels. From that I longed

                    to experience the snow and moving to the USA my snow dream was realized. However I also learn quickly

                    to fear winter and dread the winter months that I use to love growing up because those months were the

                    hottest in my country.</p>



                <p>With the shorter days and arrival of snow and black Ice, the seasonal depression. In my opinion winter has

                    become a synonymous with dread and depression. For some reason this year it has been different, I’ve

                    made a point of practicing gratitude. Here’s 8 ways how to beat the winter blues. </p>



                <h4>

                    <center><strong>Take a drive</strong></center></center>

                    <hr class="divider">

                </h4>



                <p>I found to be true that sunrise and sunset are stunning during this time of the year, especially over water.

                    Take a drive to watch the sunrise or the sunset that will sure beat those winter blues away; it doesn’t

                    have to be somewhere special as long as you are being safe. </p>



                <h4>

                    <center><strong>Be mentally prepared for winter. </strong></center>

                    <hr class="divider">

                </h4>



                <p>To beat winter blues allow to pace yourself and learn to embrace the shorter days. Knowing that they are

                    anomalies and do not last forever. You will get through this.</p>



                <h4>

                    <center><strong>Join a winter sport</strong></center>

                    <hr class="divider">

                </h4>



                <p>You can join Ice-skating snowboarding, ice fishing, skiing if you join winter sport chances of you experiencing

                    winter blues are zero! You will be busy having fun. Active bodies minds healthy minds. </p>



                <h4>

                    <center><strong>Find beauty in the winter sunlight</strong></center>

                    <hr class="divider">

                </h4>



                <p>Appreciate the beauty of natural bare trees. There is something so surreal about a snowfall; it has this

                    breath taking serenity. </p>



                <h4>

                    <center><strong>Go outside</strong></center>

                    <hr class="divider">

                </h4>



                <p>Spend some time outside. It cold and I know it no fun to be outside during this time but it always better

                    once you actually out and your body will thank you for that vitamin D. take a walk or walk your dog or

                    build a snowman. Still feeling the winter blues? </p>



                <h4>

                    <center><strong>Take daily supplements</strong></center>

                    <hr class="divider">

                </h4>



                <p>Omega-3 and vitamin D have been linked to the production of serotonin which plays a big role in regulating

                    our mood energy. So take it, I learn this while visiting Czech Republic aka Czechia, people over there

                    takes it like candy and good thing is you can’t overdose on it. These supplements will help combat those

                    winter blues</p>



                <h4>

                    <center><strong>Drink tea</strong></center>

                    <hr class="divider">

                </h4>



                <p>I grew up drinking more hot tea than coffee, it just one of those traditions that was brought over by English

                    pioneers. So It no coincidence that whenever I’m feeling under weather or sick hot tea is the first thing

                    I go for and it really helps cause of antioxidants, fluorine, caffeine and not to mention nice aroma

                </p>



                <h4>

                    <center><strong>Find strength in your community</strong></center>

                    <hr class="divider">

                </h4>



                <p>This may not work for everyone but if you have a big social circle, you may suggest to your friends a “soup

                    gathering” where all members participate in buying suppliers each gathering and meet once a month and

                    all members will get a chance to host, eat, play board games etc. It’s like a social club and at the

                    same time you develop deeper connection with them and it gives you something to look forward to in your

                    calendar.

                </p>



                <p>I also found an article while browsing the net that, Iceland people are one of the happiest people, regardless

                    that they live in snow and they deal with much shorter days. If you have made this far feel free to share

                    how do you combat the darkest of days, while staying warm and mentally healthy</p>









