                        <h2 class="text-center text-lg text-uppercase my-0">How To Prepare Your Nails For A
                            <strong>DIY Manicure</strong>
                        </h2>
                        <hr class="divider">
                        <ol>
                            <li>Soak your hands in warm water for 2 minutes and and gently dab your hands on your hand towel. </li>
                            <p></p>
                            <li>Push back your cuticles and trim the sides if necessary </li>
                            <p></p>
                            <li>Cut and file down your nails to desired length</li>
                            <p></p>
                            <li>Clean and remove any residual with warm water diluted with vinegar. </li>
                            <p></p>
                            <li>Once you’re done cleaning your nails, apply your desired bonder. The bonder basically removes any oil and dirt on your nail so that the basecoat can bond to your nail because a clean nail bed will help your nail polish last longer</li>
                            <p></p>
                            <li>Apply the basecoat and fan it for 10 minutes. Take your time when doing this step so you don't end up painting your cuticles or skin. I know there are some bonders out there like Orly that act as a basecoat but I used both just because I really want my nails to last longer.</li>
                            <p></p>
                            <li>After applying a base coat and drying it up, go ahead and apply your nail lacquer and fan dry it completely before applying a second coat and while it still tacky and damp add your diamond nuggets. </li>
                            <p></p>
                            <li> Seal with top coat fan it dry and apply another 2nd coats of topcoat if you wish but don’t get any lacquer or coats on your skin or cuticle ( can't emphasize that enough)</li>
                            <p></p>
                            <li>Apply the topcoat and fan it up for 30 minutes or more. Your nails will be sticky so if you don't have much time to wait for 30 minutes or more, use your nail dryer spray. It works like a charm and it saved me a lot of time plus it got a good coconut flavor which I like.  Finish touch Just take a cotton pad and dab it . </li>
                            <p></p>
                        </ol>
