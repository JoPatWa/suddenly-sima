                            <hr class="divider">
                            <ul>
                                <li>

                                        Take the time to prepare your nail bed and cuticles. This will help your manicure  stick to your nails just like salon manicure </li>
                                        <p></p>
                                <li>It winter, your cuticles are dry and delicate during cold conditions. This means you need to pare more attention to those cuticle when preparing for your next manicure. </li>
                                <p></p>
                                <li>Between each step, don’t touch your nails and give your nails sometime to dry up in between. </li>
                                <p></p>
                                <li>Avoid getting any coats or nail polish on your skin and cuticles to keep out dirts that may cause your nail polish to chip and appear tacky.</li>
                                <p></p>
                                <li>I normally wear shellac (I speak about this as well in my <a href="http://www.suddenlysima.com/beauty-tips-post.php">beauty tips post</a>) because it application is timely, hard to mess up and because I have a bad habit of peeling my nails therefore I have to change my manicure often, so shellac typically last me for 1.5 weeks on good days. It also holds a good quality yet very cost effective. I used O.P.I nail lacquer (bought it from my local drugstore for $10) to achieve this this nail project</li>
                                <p></p>
                                <li>Once you are satisfied that your nails are completely dry, apply castor oil on your cuticles and skin surrounding your nails to lock in moisture and prevent dryness.</li>
                                <p></p>
                                <li>I really like this look and the lacquer gives off this well balanced shine and gloss. I always try my best to resist the urge of peeling my nails.</li>
